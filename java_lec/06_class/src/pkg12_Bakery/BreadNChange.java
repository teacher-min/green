package pkg12_Bakery;

public class BreadNChange {

  private int bread;
  private int change;
  
  public BreadNChange(int bread, int change) {
    super();
    this.bread = bread;
    this.change = change;
  }
  
  public int getBread() {
    return bread;
  }

  public void setBread(int bread) {
    this.bread = bread;
  }

  public int getChange() {
    return change;
  }

  public void setChange(int change) {
    this.change = change;
  }
  
}
